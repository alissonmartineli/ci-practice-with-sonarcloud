const mathOperations = {
    sum: (a: number, b: number): number => {
        return a + b
    },

    diff: (a: number, b: number): number => {
        return a - b
    },

    product: (a: number, b: number): number => {
        return a * b
    },

    divide: (a: number, b: number): number => {
        return a / b
    }
}

export { mathOperations }
